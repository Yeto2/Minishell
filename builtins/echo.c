#include "../minishell.h"

void ft_echo(char *s, int a)
{
	int	i;

	i = 0;
	if (i)
	{
		while (s[i])
		{
			write(1,&s[i],1);
			i++;
		}
		write(1,"\n",1);
	}
	else
	{
		while (s[i])
		{
			write(1,&s[i],1);
			i++;
		}
	}
}